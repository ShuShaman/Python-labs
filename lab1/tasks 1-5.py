# Напишите скрипт,  который  преобразует  введенное  с  клавиатуры
# вещественное число в денежный формат. Например, число 12,5 должно быть
# преобразовано  к  виду «12  руб.  50 коп.». В  случае  ввода отрицательного
#  числа  выдайте сообщение«Некорректный  формат!»путемобработки исключения в коде


def task1():
    num = float(input("Введите вещественное число "))
    if num < 0:
        raise Exception('Uncorrect format')
    else:
        print(int(num // 1), "руб.", round((num - num // 1) * 100), "коп.")


