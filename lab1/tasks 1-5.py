# Напишите  скрипт,  который  позволяет  ввести  с  клавиатуры
# номер дебетовой карты (16 цифр) и выводит номер в скрытом виде:
# первые и последние 4 цифры отображены нормально, а между ними
# –символы «*»(например, 5123 **** **** 1212).


def task3():
    card_number = input('Введите номер карты (16 цифр): ').replace(" ", "")
    print(card_number[:4] + '*' * 8 + card_number[-4:]) if len(card_number) == 16 else print('Неверный номер')


