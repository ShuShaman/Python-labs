# запятыми или пробелами.Например,  если пользователь введет строку «город Донецк,река Кальмиус»,
# результирующая  строка  должна  выглядеть  так:  «город ДОНЕЦК, река КАЛЬМИУС».


def task5():
    text = input().split(' ')
    print(' '.join([i.upper() if i == i.title() else i for i in text]))


